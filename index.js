function scuberGreetingForFeet(n){
  if (n === 199) {
    return 'This one is on me!';
  } else if (n === 2001) {
    return 'I will gladly take your thirty bucks.';
  } else if (n === 2501) {
    return 'No can do.';
  }
}

function ternaryCheckCity(n){
  if (n === "NYC") {
    return ('Ok, sounds good.');
  } else if (n === ('Pittsburgh')) {
    return 'No go.';
  }
}

function switchOnCharmFromTip(t){
  if (t === 'generous') {
    return 'Thank you so much.';
  } else if (t === 'not as generous') {
    return 'Thank you.';
  } else if (t === 'thanks for everything') {
    return "Bye.";
  }
}